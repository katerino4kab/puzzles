import random

rebuses = {
    "кот": "Маленький пушистый друг, который ловит мышей.",
    "собака": "Верный друг человека, который лает.",
    "птица": "Существа с перьями, которые могут летать.",
    "рыба": "Животное, которое живет в воде и не дышит воздухом.",
    "слон": "Большое животное с длинным хоботом.",
    "жираф": "Высокое животное с длинной шеей.",
    "крокодил": "Большой рептилия, обитающая в воде.",
    "зебра": "Полосатое животное, похожее на лошадь.",
    "тигр": "Большая кошка с полосами.",
    "панда": "Медлительное черно-белое животное, питающееся бамбуком."
}


def get_hint(word):
    return rebuses[word]


def play_game():
    print("Добро пожаловать в игру 'Угадай ребус'!")

    score = 0
    rounds = 3

    for _ in range(rounds):
        word, hint = random.choice(list(rebuses.items()))
        print(f"\nПодсказка: {hint}")

        guess = input("Ваш ответ (или введите 'подсказка' для дополнительной подсказки): ").strip().lower()

        if guess == 'подсказка':
            print(f"Дополнительная подсказка: {get_hint(word)}")
            guess = input("Попробуйте угадать снова: ").strip().lower()

        if guess == word:
            print("Поздравляю! Вы угадали слово!")
            score += 1
        else:
            print(f"Увы, правильный ответ: {word}")

    print(f"\nИгра окончена! Ваш счет: {score}/{rounds}")


if __name__ == "__main__":
    play_game()
